    Sprawozdanie z działalności Sekcji FUTURE3D
    Koła Naukowego Instytutu Informatyki Stosowanej
    Państwowej Wyższej Szkoły Zawodowej w Elblągu
    w roku akademickim 2017/2018
</div>
<div>
    <p>Celem <b>Sekcji FUTURE3D</b> Koła Naukowego Instytutu Informatyki Stosowanej im. Krzysztofa Brzeskiego Państwowej Wyższej Szkoły Zawodowej w Elblągu jest rozwijanie kreatywnego myślenia.</p>
    <p>Misją naszej Sekcji jest inicjowanie i realizowanie prac wspomagających doskonalenie procesu dydaktyczno-wychowawczego PWSZ w celu przygotowania studentów do sprawnego funkcjonowania w społeczeństwie informacyjnym, w którym wykorzystywać będą wiedzę i umiejętności zdobyte na Uczelni.</p>
    <p>W roku akademickim 2017/2018 w ramach Sekcji realizowane były następujące zadania:</p>
</div>

<div><p>
        1.	Spotkania Sekcji odbywały się regularnie, co czwartek, w godzinach: 14.30 ÷ 19.00 w Laboratorium Sztucznej Inteligencji i Szybkiego Prototypowania mieszczącym się w sali nr 112/113 budynku Instytutu Informatyki Stosowanej im. Krzysztofa Brzeskiego w Elblągu.
    </p>
    <p>
        2.	W ramach spotkań Sekcji prowadzone były zajęcia poświęcone:<br>
        •	budowie lądowych robotów mobilnych,<br>
        •	budowie robotów latających – dronów,<br>
        •	programowaniu układów sterowania,<br>
        •	transmisji sygnałów z nadajników do odbiorników układów sterowania oraz torów wizyjnych,<br>
        •	budowie stacji bazowych robotów mobilnych,<br>
        •	przetwarzaniu obrazów zarejestrowanych przez kamery zainstalowane na pokładach robotów mobilnych,<br>
        •	autonomicznym robotom mobilnym, <br>
        •	tworzeniu modeli 3D w systemie CATIA,<br>
        •	fotogrametrycznym metodom generowania chmury punktów,<br>
        •	skanerom 3D na bazie światła laserowego,<br>
        •	skanerom 3D na bazie światła strukturalnego,
    </p>
    <p>
        3.	Sekcja aktywnie uczestniczyła w Festiwalu Nauki PWSZ w Elblągu prowadząc warsztaty z tematu: Układy mikroprocesorowe – roboty mobilne oraz techniki skanowania obiektów do postaci 3D.
    </p>
    <p>
        4.	Zbudowano szereg innowacyjnych konstrukcji robotów mobilnych. Sekcja organizowała wśród swoich członków zawody lądowych robotów mobilnych.
    </p>
</div>

